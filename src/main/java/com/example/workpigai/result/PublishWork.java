package com.example.workpigai.result;

public class PublishWork {

    int id;
    int worksDetailId;
    String worksDetailWorkTitle;
    String worksDetailPublishContent;
    String worksDetailAnswer;
    int teacherId;
    int classId;
    String endTime;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getWorksDetailId() {
        return worksDetailId;
    }

    public void setWorksDetailId(int worksDetailId) {
        this.worksDetailId = worksDetailId;
    }

    public String getWorksDetailWorkTitle() {
        return worksDetailWorkTitle;
    }

    public void setWorksDetailWorkTitle(String worksDetailWorkTitle) {
        this.worksDetailWorkTitle = worksDetailWorkTitle;
    }

    public String getWorksDetailPublishContent() {
        return worksDetailPublishContent;
    }

    public void setWorksDetailPublishContent(String worksDetailPublishContent) {
        this.worksDetailPublishContent = worksDetailPublishContent;
    }

    public String getWorksDetailAnswer() {
        return worksDetailAnswer;
    }

    public void setWorksDetailAnswer(String worksDetailAnswer) {
        this.worksDetailAnswer = worksDetailAnswer;
    }

    public int getTeacherId() {
        return teacherId;
    }

    public void setTeacherId(int teacherId) {
        this.teacherId = teacherId;
    }

    public int getClassId() {
        return classId;
    }

    public void setClassId(int classId) {
        this.classId = classId;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }
}
